//  Assignment-04_Problem-01: Write a program to print MySirG 5 times on the screen.

#include <stdio.h>
#include <stdlib.h>

int main()
{
    // PROGRAM OPENING...
    system("cls");
    printf("This is Assignment-04_Problem-01: Will Print, MySirG 5 Times On The Screen...");

    int i;

    // DATA PROCESSING...
    printf("\n\n");
    printf("But, We Want To Print, \"MySirG\" 5 Times On The Screen, Right...");
    printf("\n\n");

    for (i = 0; i < 5; i++)
    {
        printf("MySirG");
        printf("\n");
    }

    // DATA OUTPUT...
    printf("\n\n");
    printf("Hence...");
    printf("\n");
    printf("This Was The Program, To Print, \"MySirG\" 5 Times On The Screen");

    // FORMALITY...
    printf("\n\n");
    printf("Thanks For Using This Program...\n");
    printf("Hope We Will Meet Again...\n");
    printf("Bye...");

    // PROGRAM CLOSING...
    printf("\n\n");
    return 0;
}